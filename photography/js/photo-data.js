// Helper function to get metadata by photo ID
function getPhotoMetadata(photoId) {
    return PHOTO_METADATA[photoId] || {
        title: 'Untitled Photo',
        description: 'No description available',
        category: 'uncategorized',
        type: 'unknown',
        date: new Date().toISOString().split('T')[0],
        camera: 'Unknown',
        settings: 'Unknown',
        tags: [],
        featured: false
    };
}

// Helper function to get all photos of a specific type
function getPhotosByType(type) {
    return Object.keys(PHOTO_METADATA)
        .filter(photoId => PHOTO_METADATA[photoId].type === type)
        .map(photoId => ({
            id: photoId,
            ...PHOTO_METADATA[photoId]
        }));
}

// Helper function to get all photos by category
function getPhotosByCategory(category) {
    return Object.keys(PHOTO_METADATA)
        .filter(photoId => PHOTO_METADATA[photoId].category === category)
        .map(photoId => ({
            id: photoId,
            ...PHOTO_METADATA[photoId]
        }));
}